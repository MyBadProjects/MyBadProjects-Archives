    String calc1 = "";
    String calc2 = "";
    int currentCalc = 1;
    int method = 0;
    int decimalPlaced = 0;
    float output;
    
    private void addNumber(String number){
        if (currentCalc == 1){
            calc1 += number;
            updateTextField(calc1);
        }
        else {
            calc2 += number;
            updateTextField(calc2);
        }
    }
    
    private void addCalc(int methodMode){
        method = methodMode;
        
        currentCalc = 2;
        
        updateTextField(calc2);
        
        decimalPlaced = 0;
    }
    
    private void finishCalc(){
        if (currentCalc == 2){
            float c1 = Float.valueOf(calc1);
            float c2 = Float.valueOf(calc2);
            
            switch (method) {
                case 1:
                    output = c1 + c2;
                    updateTextField(String.valueOf(output));
                    calc1 = "";
                    calc2 = "";
                    decimalPlaced = 0;
                    method = 0;
                    currentCalc = 1;
                    rollOver(output);
                    break;
                case 2:
                    output = c1 - c2;
                    updateTextField(String.valueOf(output));
                    calc1 = "";
                    calc2 = "";
                    decimalPlaced = 0;
                    method = 0;
                    currentCalc = 1;
                    rollOver(output);
                    break;
                case 3:
                    output = c1 / c2;
                    updateTextField(String.valueOf(output));
                    calc1 = "";
                    calc2 = "";
                    decimalPlaced = 0;
                    method = 0;
                    currentCalc = 1;
                    rollOver(output);
                    break;
                case 4:
                    output = c1 * c2;
                    updateTextField(String.valueOf(output));
                    calc1 = "";
                    calc2 = "";
                    decimalPlaced = 0;
                    method = 0;
                    currentCalc = 1;
                    rollOver(output);
                    break;
                default:
                    break;
            }
        }
        
    }
    
    private void addDecimal(){
        if (decimalPlaced != 1){
            decimalPlaced = 1;
            
            if (currentCalc == 1){
                calc1 += ".";
                updateTextField(calc1);
            }
            else
            {
                calc2 += ".";
                updateTextField(calc2);
            }
        }
    }
    
    private void updateTextField(String Input){
        jTextField1.setText(Input);
    }
    
    private void rollOver(float Input){
        if (jCheckBox1.isSelected() == true){
            calc1 = String.valueOf(Input);
            updateTextField(calc1);
        }
    }
    
    private void clearCalc(){
        calc1 = "";
        calc2 = "";
        currentCalc = 1;
        method = 0;
        decimalPlaced = 0;
        output = 0;
        updateTextField(calc1);
    }
    
